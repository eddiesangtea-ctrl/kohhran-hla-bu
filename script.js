let current = 0;
let scrollInterval=null;
let fontSize=20;

/* Create list */
const list=document.getElementById("songList");

songs.forEach((song,i)=>{
    let div=document.createElement("div");
    div.className="song";
    div.textContent=`${song.number}. ${song.title}`;
    div.onclick=()=>loadSong(i);
    list.appendChild(div);
});

function loadSong(i){
    current=i;
    document.getElementById("songTitle").textContent =
        `${songs[i].number}. ${songs[i].title}`;

    document.getElementById("lyrics").textContent =
        songs[i].lyrics;

    document.getElementById("lyrics").scrollTop=0;
}

function nextSong(){
    current=(current+1)%songs.length;
    loadSong(current);
}

function prevSong(){
    current=(current-1+songs.length)%songs.length;
    loadSong(current);
}

/* Search */
function searchSong(){
    let value = document.getElementById("search").value.trim().toLowerCase();

    let allSongs = document.querySelectorAll(".song");

    // If user types a number → open song directly
    if(!isNaN(value) && value !== ""){
        let number = parseInt(value);

        let index = songs.findIndex(song => song.number === number);

        if(index !== -1){
            loadSong(index);
        }
    }

    // Filter song list (title or number)
    allSongs.forEach((songDiv, i)=>{
        let text = songDiv.textContent.toLowerCase();

        songDiv.style.display =
            text.includes(value) ? "block" : "none";
    });
}

/* Auto Scroll */
function autoScroll(){
    if(scrollInterval){
        clearInterval(scrollInterval);
        scrollInterval=null;
        return;
    }

    scrollInterval=setInterval(()=>{
        document.getElementById("lyrics").scrollTop+=1;
    },60);
}

/* Font Size */
function changeFont(size){
    fontSize+=size;
    document.getElementById("lyrics").style.fontSize =
        fontSize+"px";
}

/* Dark Mode */
function toggleDarkMode(){
    document.body.classList.toggle("dark");
}

/* Favorites */
function favoriteSong(){
    let favs=JSON.parse(localStorage.getItem("favorites"))||[];
    favs.push(songs[current].number);
    localStorage.setItem("favorites",JSON.stringify(favs));
    alert("Added to favorites ❤️");
}

